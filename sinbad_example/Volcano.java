public class Volcano{

    private String country;
    private String name;
    private String region;
    
    public Volcano (String cntry, String nm, String reg){
        country=cntry;
        name=nm;
        region = reg;
    }
    
    public String getCountry(){
        return country;
    }
    
    public String getName(){
        return name;
    }
    
    public String getRegion(){
        return region;
    }

    
    
}            
        
    